    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $certificate = Certificates::where('id', $id)->first();
        $today = Carbon::now()->format('d/m/Y');
        $ano  = Carbon::now()->format('Y');
        $filename =  'Certificado'.($certificate->model->firstname).($certificate->model->lastname);
        $pdf = PDF::loadView('cliente.certificates',['certificate'=>$certificate,'today'=>$today,'filename'=>$filename,'ano'=>$ano])->setPaper('a4', 'landscape');
        return $pdf->stream($filename.'.pdf');
        //return $pdf->download($filename.'.pdf');
    }

